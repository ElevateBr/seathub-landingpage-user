// Variável global para controlar o idioma atual
let currentLanguage = 'pt';

// Função principal de tradução
function translatePage(lang) {
    currentLanguage = lang;
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        
        // Lógica especial para o H1: só traduz se NÃO for PT-BR
        if (key === 'hero-title') {
            if (lang === 'pt') {
                // Para PT-BR, mantém o conteúdo original do HTML (não faz nada)
                return;
            } else if (translations[lang] && translations[lang][key]) {
                // Para EN/ES, força a tradução
                element.innerHTML = translations[lang][key];
            }
            return;
        }
        
        // Para outros elementos, traduz normalmente
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Atualizar botões de idioma
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    document.querySelector(`[data-lang="${lang}"]`).classList.add('active');
}

// Função para obter tradução específica
function getTranslation(key, lang = currentLanguage) {
    return translations[lang] && translations[lang][key] ? translations[lang][key] : key;
}

